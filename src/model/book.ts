
export class book {
    isbn:number;
    titulo: string;
    editorial: string;
    precio: number;
    genero: string;
    descripcion: string;

    constructor(isbn: number,
        titulo: string,
        editorial: string,
        precio: number,
        genero: string,
        descripcion: string,
    ) {
        this.isbn = isbn;
        this.titulo = titulo;
        this.editorial = editorial;
        this.precio = precio;
        this.genero = genero;
        this.descripcion = descripcion;
    }
}
































































