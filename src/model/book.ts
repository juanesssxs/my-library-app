export class Book {
    titulo: string;
    editorial: string;
    precio: number;
    genero: string;
    descripcion: string;

    constructor(titulo: string,
                editorial: string,
                precio: number,
                genero: string,
                descripcion: string) {
        this.titulo = titulo;
        this.editorial = editorial;
        this.precio = precio;
        this.genero = genero;
        this.descripcion = descripcion;
    }
}































































